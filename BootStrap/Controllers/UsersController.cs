using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using BootStrap.Models;
using Microsoft.AspNetCore.Http;

namespace BootStrap.Controllers
{
    public class UsersController : Controller
    {
        private readonly rbintlContext _context;

        public UsersController(rbintlContext context)
        {
            _context = context;
        }

        // GET: Users
        public async Task<IActionResult> Index()
        {
            return View(await _context.User.ToListAsync());
        }

        [HttpGet]
        public IActionResult Login()
        {
            return View();
        }
        //bool flag = new bool();
        [HttpPost]
        public IActionResult Login(string UserFirstName, string UserPassword)
        {
            
            User U = _context.User.Where(u => u.UserFirstName == UserFirstName && u.UserPassword == UserPassword).FirstOrDefault();
            
            if(U == null)
            {
                ViewBag.error = "Invalid User";
            }
            else
            {
                HttpContext.Session.SetString("UserName", U.UserFirstName);
              //  flag = true;
                ViewBag.Done = "It's working";
            }


            return RedirectToAction("ActionsAdmin");
        }

        public IActionResult ActionsAdmin()
        {
            return View();
        }

        public IActionResult Logout()
        {
           
                HttpContext.Session.Clear();
              //  flag = false;
            
            return RedirectToAction("Index");
        }
    

        // GET: Users/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var user = await _context.User
                .FirstOrDefaultAsync(m => m.UserId == id);
            if (user == null)
            {
                return NotFound();
            }

            return View(user);
        }

        // GET: Users/Create
        public IActionResult Create()
        {
            return View();
        }

        // POST: Users/Create
        // To protect from overposting attacks, enable the specific properties you want to bind to, for 
        // more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Create([Bind("UserId,UserEmail,UserPassword,UserFirstName,UserLastName,UserCity,UserState,UserZip,UserEmailVerified,UserRegisterationDate,UserVerificationCode,UserIp,UserPhone,UserFax,UserCountry,UserAddress,UserAddress2,UserRole")] User user)
        {
            if (ModelState.IsValid)
            {
                _context.Add(user);
                await _context.SaveChangesAsync();
                return RedirectToAction(nameof(Index));
            }
            return View(user);
        }

        // GET: Users/Edit/5
        public async Task<IActionResult> Edit(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var user = await _context.User.FindAsync(id);
            if (user == null)
            {
                return NotFound();
            }
            return View(user);
        }

        // POST: Users/Edit/5
        // To protect from overposting attacks, enable the specific properties you want to bind to, for 
        // more details, see http://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Edit(int id, [Bind("UserId,UserEmail,UserPassword,UserFirstName,UserLastName,UserCity,UserState,UserZip,UserEmailVerified,UserRegisterationDate,UserVerificationCode,UserIp,UserPhone,UserFax,UserCountry,UserAddress,UserAddress2")] User user)
        {
            if (id != user.UserId)
            {
                return NotFound();
            }

            if (ModelState.IsValid)
            {
                try
                {
                    _context.Update(user);
                    await _context.SaveChangesAsync();
                }
                catch (DbUpdateConcurrencyException)
                {
                    if (!UserExists(user.UserId))
                    {
                        return NotFound();
                    }
                    else
                    {
                        throw;
                    }
                }
                return RedirectToAction(nameof(Index));
            }
            return View(user);
        }

        // GET: Users/Delete/5
        public async Task<IActionResult> Delete(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var user = await _context.User
                .FirstOrDefaultAsync(m => m.UserId == id);
            if (user == null)
            {
                return NotFound();
            }

            return View(user);
        }

        // POST: Users/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> DeleteConfirmed(int id)
        {
            var user = await _context.User.FindAsync(id);
            _context.User.Remove(user);
            await _context.SaveChangesAsync();
            return RedirectToAction(nameof(Index));
        }

        private bool UserExists(int id)
        {
            return _context.User.Any(e => e.UserId == id);
        }
    }
}
